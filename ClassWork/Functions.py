def sum(a, b):
    #Функция принимает 2 значения и возращает их сумму
    return a + b


def difference(a, b):
    # Функция принимает 2 значения и возращает их разность
    return a - b


def multiply(a, b):
    # Функция принимает 2 значения и возращает их произведение
    return a * b


def division(a, b):
    # Функция принимает 2 значения и возвращает результат их деления
    return a / b
